def create_complex_structure(output_dir):
    """EPUB with nested TOC, multiple sections, and rich metadata."""
    book = epub.EpubBook()
    book.set_identifier("urn:uuid:ebooklib-complex-001")
    book.set_title("Complex Structure Test")
    book.set_language("en")
    book.add_author("First Author")
    book.add_author("Second Author")
    book.add_metadata("DC", "description", "A complex structure test with nested TOC.")
    book.add_metadata("DC", "publisher", "Test Publisher")
    book.add_metadata("DC", "rights", "Public Domain")
    book.add_metadata("DC", "subject", "Testing")
    book.add_metadata("DC", "date", "2025-01-01")

    style = epub.EpubItem(
        uid="style",
        file_name="style/main.css",
        media_type="text/css",
        content=b"body { font-family: Georgia, serif; margin: 2em; }\n"
        b"h1, h2, h3 { color: #333; }\n"
        b"table { border-collapse: collapse; width: 100%; }\n"
        b"th, td { border: 1px solid #ddd; padding: 8px; }\n",
    )
    book.add_item(style)

    # Part 1
    p1c1 = epub.EpubHtml(title="Chapter 1", file_name="ch01.xhtml", lang="en")
    p1c1.content = """<html xmlns="http://www.w3.org/1999/xhtml">
<head><title>Chapter 1</title><link rel="stylesheet" href="style/main.css" type="text/css"/></head>
<body>
<h1>Part I: Fundamentals</h1>
<h2>Chapter 1: Getting Started</h2>
<p>This chapter introduces the fundamental concepts.</p>
<h3>Section 1.1</h3>
<p>A subsection with <em>emphasis</em> and <strong>strong</strong> text.</p>
<h3>Section 1.2</h3>
<p>Another subsection with a nested list:</p>
<ul>
<li>Level 1
  <ul>
  <li>Level 2a</li>
  <li>Level 2b
    <ul><li>Level 3</li></ul>
  </li>
  </ul>
</li>
<li>Another level 1</li>
</ul>
</body>
</html>"""
    p1c1.add_item(style)
    book.add_item(p1c1)

    p1c2 = epub.EpubHtml(title="Chapter 2", file_name="ch02.xhtml", lang="en")
    p1c2.content = """<html xmlns="http://www.w3.org/1999/xhtml">
<head><title>Chapter 2</title><link rel="stylesheet" href="style/main.css" type="text/css"/></head>
<body>
<h2>Chapter 2: Data Structures</h2>
<table>
<thead><tr><th>Structure</th><th>Access</th><th>Insert</th><th>Delete</th></tr></thead>
<tbody>
<tr><td>Array</td><td>O(1)</td><td>O(n)</td><td>O(n)</td></tr>
<tr><td>Linked List</td><td>O(n)</td><td>O(1)</td><td>O(1)</td></tr>
<tr><td>Hash Table</td><td>O(1)</td><td>O(1)</td><td>O(1)</td></tr>
</tbody>
</table>
</body>
</html>"""
    p1c2.add_item(style)
    book.add_item(p1c2)

    # Part 2
    p2c1 = epub.EpubHtml(title="Chapter 3", file_name="ch03.xhtml", lang="en")
    p2c1.content = """<html xmlns="http://www.w3.org/1999/xhtml">
<head><title>Chapter 3</title><link rel="stylesheet" href="style/main.css" type="text/css"/></head>
<body>
<h1>Part II: Advanced Topics</h1>
<h2>Chapter 3: Special Characters</h2>
<p>HTML entities: &amp; &lt; &gt; &quot;</p>
<p>Unicode: em dash \u2014 en dash \u2013 ellipsis \u2026</p>
<p>Symbols: \u00a9 \u2122 \u00ae \u00b1 \u2260 \u2264 \u2265</p>
</body>
</html>"""
    p2c1.add_item(style)
    book.add_item(p2c1)

    p2c2 = epub.EpubHtml(title="Conclusion", file_name="conclusion.xhtml", lang="en")
    p2c2.content = """<html xmlns="http://www.w3.org/1999/xhtml">
<head><title>Conclusion</title><link rel="stylesheet" href="style/main.css" type="text/css"/></head>
<body>
<h2>Conclusion</h2>
<p>This complex document tested nested TOC, subdirectories, tables, and rich metadata.</p>
</body>
</html>"""
    p2c2.add_item(style)
    book.add_item(p2c2)

    # Nested TOC
    book.toc = [
        (epub.Section("Part I: Fundamentals"), [
            epub.Link("ch01.xhtml", "Chapter 1: Getting Started", "ch01"),
            epub.Link("ch02.xhtml", "Chapter 2: Data Structures", "ch02"),
        ]),
        (epub.Section("Part II: Advanced Topics"), [
            epub.Link("ch03.xhtml", "Chapter 3: Special Characters", "ch03"),
            epub.Link("conclusion.xhtml", "Conclusion", "conclusion"),
        ]),
    ]
    book.add_item(epub.EpubNcx())
    book.add_item(epub.EpubNav())
    book.spine = ["nav", p1c1, p1c2, p2c1, p2c2]

    path = os.path.join(output_dir, "ebooklib-complex-structure.epub")
    epub.write_epub(path, book)
    return path


def create_semantic_html5(output_dir):
    """EPUB with HTML5 semantic elements."""
    book = epub.EpubBook()
    book.set_identifier("urn:uuid:ebooklib-semantic-001")
    book.set_title("Semantic HTML5 Content")
    book.set_language("en")
